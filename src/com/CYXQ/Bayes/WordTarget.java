package com.CYXQ.Bayes;

public class WordTarget {

	private String cwn;	//目标词
	private int num = 1;	//个数
	private double percent; //概率
	private String result; //结果
	
	
	/*
	 * get set
	 */
	public String getCwn1() {
		return cwn;
	}
	public void setCwn1(String cwn) {
		this.cwn = cwn;
	}
	public int getNum1() {
		return num;
	}
	public void setNum1(int num) {
		this.num = num;
	}
	public double getPercent1() {
		return percent;
	}
	public void setPercent1(double percent) {
		this.percent = percent;
	}
	public String getResult1() {
		return result;
	}
	public void setResult1(String result) {
		this.result = result;
	}
	
	
}
